package com.solid.enums;

public enum EClientPriority {
    MVP, // Le client le plus précieux, qui a un impact majeur pour le restaurant (que ce
    // soit par sa fidélité, son influence ou son rôle dans le succès de
    // l’établissement).
    VIP, // Clients privilégiés, bénéficient d'un traitement spécial.
    LOYAL, // Clients réguliers, qui reviennent souvent et sont très appréciés.
    FAMILY, // Groupes familiaux, nécessitant une attention particulière (enfants, repas
            // adaptés, etc.).
    NORMAL; // Clients standards, sans traitement particulier.
}
